    static function get_html_theme_base_icon_list() {
        $iconlist = array();
        if ( $_POST['floral_icon_picker_lib'] == '9wpthemes' ) {
            $iconlist = self::get_theme_base_icons();
        }
	
	    if ( $_POST['floral_icon_picker_lib'] == 'floral' ) {
		    $iconlist = self::get_theme_floral_icons();
	    }

        if ( $_POST['floral_icon_picker_lib'] == 'font-awesome' ) {
            $iconlist = self::get_fontawesome_icons();
        }

        $output = '';
        foreach ( $iconlist as $icon ) {
            $output .= sprintf( '<div class="icon-container" title="%s"><i class="%s"></i></div>', reset( $icon ), array_keys( $icon )[0] );
        }
        $output = sprintf( '<div class="floral-icon-list">%s</div>', $output );

        return $output;
    }
